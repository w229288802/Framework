package com.welge.framework.vo.ztree;

public class JsonZtree {
	private String id;
	private Long pid;
	private String name;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Long getpId() {
		return pid;
	}
	public void setpId(Long pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
